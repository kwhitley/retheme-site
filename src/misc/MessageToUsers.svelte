  <h2>A message to my fearless users</h2>
  <p>
    Thank you so much for being one of the early testers of Retheme! While I would selfishly make this extension for myself anyway, your support
    and encouragement will go a long way to keep me motivated!
  </p>

  <p>
    While this extension is still rapidly evolving, I'd encourage you to pin the extension to your toolbar.
    This allows you to see when a site is being themed (you'll see a green "T" badge over the icon), and
    gives you access to the extension controls to turn features on/off, or to request a theme for a site you're visiting.
  </p>

  <p>If you're a fan, consider sharing it on social media or telling your friends!</p>

